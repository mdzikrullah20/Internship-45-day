// Object manipulation
// objects ko modify, update, add, remove, merge, and restructure karna. '
// JavaScript me objects ko manipulate karne ke multiple tarike hain.


// Add New Property
let person = { name: "Zikrullah", age:21};
person.city = "Bhopal";  
console.log(person)


// Update Existing Property
person.email = "mdzikrullah@gmail.com"
console.log(person.age)


// Delete a Property
delete person.age;
console.log(person); 
