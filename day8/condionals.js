// Basic switch Statement
// switch statement multiple conditions ko handle karne ke liye use hota hai
let grade = 'B';

switch (grade) {
    case 'A':
        console.log("Excellent");
        break;
    case 'B':
        console.log("Good");
    case 'C':
        console.log("Average");
        break;
    default:
        console.log("Invalid grade");
}

