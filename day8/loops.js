// 1. Entry Controlled Loop
// Condition pehle check hoti hai, phir loop execute hota hai.
// 2. Exit Controlled Loop
// Pehle loop execute hota hai, phir condition check hoti hai.

// for loop
for (let i = 0; i < 5; i++) {
    console.log(i);
}
// while loop
let i = 0;
while (i < 5) {
    console.log(i);
    i++;  //
}

// do while loop 
let z = 0;
do {
    console.log(z);
    z++;
} while (z < 5);
